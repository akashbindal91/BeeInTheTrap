<?php

class Calculations
{

    function get_dividend($a, $b)
    {
        return $a / $b;
    }

    function get_remainder($a, $b)
    {
        return $a % $b;
    }

    function get_multiply($a, $b)
    {
        return $a * $b;
    }

    function get_difference($a, $b)
    {
        return $a - $b;
    }

    function get_addition($a, $b)
    {
        return $a + $b;
    }
}
